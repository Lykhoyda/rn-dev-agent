import assert from 'node:assert/strict';
import test from 'node:test';
import { existsSync, readFileSync, readdirSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const root = resolve(here, '../../../..');
const codex = join(root, 'packages/codex-plugin');
const sourceMap = JSON.parse(
  readFileSync(join(root, 'packages/shared-agent-knowledge/source-map.json'), 'utf8'),
) as {
  hostAdaptations: {
    codex: { adaptedDomainSkills: string[]; adaptedCommands: string[]; commandSkills: string[] };
  };
};
const contract = sourceMap.hostAdaptations.codex;

function directories(path: string): string[] {
  return readdirSync(path, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .map((entry) => entry.name)
    .sort();
}

test('GH-575 generated Codex skill inventory is exactly 25 with migration disabled', () => {
  assert.equal(contract.adaptedDomainSkills.length, 10);
  assert.equal(contract.adaptedCommands.length, 15);
  assert.deepEqual(contract.commandSkills, contract.adaptedCommands);
  const expected = [...contract.adaptedDomainSkills, ...contract.commandSkills].sort();
  assert.equal(new Set(expected).size, 25);
  assert.deepEqual(directories(join(codex, 'skills')), expected);
  const manifest = JSON.parse(readFileSync(join(codex, '.codex-plugin/plugin.json'), 'utf8')) as {
    commands?: unknown;
  };
  assert.deepEqual(manifest.commands, []);
  assert.equal(existsSync(join(codex, '.codex-plugin/migrated-command-skills')), false);
});

test('GH-575 every command adapter is generated, explicit-only, and package-relative', () => {
  for (const name of contract.commandSkills) {
    const skill = readFileSync(join(codex, 'skills', name, 'SKILL.md'), 'utf8');
    const metadata = readFileSync(join(codex, 'skills', name, 'agents/openai.yaml'), 'utf8');
    assert.equal(skill.startsWith('---\n'), true, `${name} must start with YAML frontmatter`);
    assert.match(skill, /GENERATED by scripts\/build-host-runtimes\.ts/);
    assert.match(skill, new RegExp(`name: ${name}(?:\\n|$)`));
    assert.match(skill, new RegExp(`\\$rn-dev-agent:${name}`));
    assert.match(skill, new RegExp(`\\.\\./\\.\\./commands/${name}\\.md`));
    assert.match(skill, /conceptual \*\*request\*\*/);
    assert.match(metadata, /allow_implicit_invocation:\s*false/);
    assert.match(metadata, new RegExp(`\\$rn-dev-agent:${name}`));
  }
});

