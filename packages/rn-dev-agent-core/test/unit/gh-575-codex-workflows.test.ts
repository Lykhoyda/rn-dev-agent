test('GH-575 Codex runtime instructions contain no Claude substitution or cache-root fallback', () => {
  const files: string[] = [];
  const walk = (path: string): void => {
    for (const entry of readdirSync(path, { withFileTypes: true })) {
      const child = join(path, entry.name);
      if (entry.isDirectory()) walk(child);
      else if (/\.(?:md|ya?ml)$/.test(entry.name)) files.push(child);
    }
  };
  walk(join(codex, 'commands'));
  walk(join(codex, 'skills'));
  files.push(join(codex, 'src/AGENTS-MD-TEMPLATE.md'));
  const joined = files.map((file) => readFileSync(file, 'utf8')).join('\n');
  for (const forbidden of [
    /\$ARGUMENTS/,
    /\$\{ARGUMENTS/,
    /CLAUDE_PLUGIN_ROOT/,
    /RN_DEV_AGENT_CODEX_PLUGIN_ROOT/,
    /CODEX_PLUGIN_ROOT/,
    /\/rn-dev-agent:/,
    /mcp__plugin_rn-dev-agent_cdp__/,
    /\/plugin (?:install|update)/,
    /find [^\n]*plugins\/cache/,
  ]) {
    assert.doesNotMatch(joined, forbidden);
  }
});

